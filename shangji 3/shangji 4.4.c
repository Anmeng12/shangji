#include <stdio.h>

int main() 
{
    int num, hundreds, tens, units;
    printf("请输入一个三位正整数：");
    scanf_s("%d", &num);

    //提取百 十 个位数字
    hundreds = num / 100;
    tens = (num / 10) % 10;
    units = num % 10;

    if (hundreds * hundreds * hundreds + tens * tens * tens + units * units * units == num) 
    {
        printf("%d是水仙花数\n", num);
    }
    else 
    {
        printf("%d不是水仙花数\n", num);
    }

    return 0;
}